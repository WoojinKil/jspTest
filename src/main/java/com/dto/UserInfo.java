package com.dto;

public class UserInfo {

	private String lgnId;
	private String pw;
	private String name;
	
	
	public String getLgnId() {
		return lgnId;
	}
	public void setLgnId(String lgnId) {
		this.lgnId = lgnId;
	}
	public String getPw() {
		return pw;
	}
	public void setPw(String pw) {
		this.pw = pw;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	
}
